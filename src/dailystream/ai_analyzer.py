def _warn_no_anthropic() -> None:
    logger.warning(
        "anthropic SDK is not installed — AI analysis is disabled. "
        "Install with: pip install 'dailystream[ai]'"
    )


# ---------------------------------------------------------------------------
# Data classes
# ---------------------------------------------------------------------------


@dataclass
class AnalysisResult:
    """A single AI analysis result.

    Linked to the corresponding ``context.json`` entry via
    ``entry_index`` (array position) and ``timestamp`` (double-check).
    """

    entry_index: int
    timestamp: str  # must match the entry's timestamp
    input_type: str  # "image" | "url"
    description: str  # AI-generated description
    category: str  # coding / design / browsing / document / communication / other
    key_elements: list[str] = field(default_factory=list)
    analyzed_at: str = ""
    status: str = "completed"  # "completed" | "failed"
    error: str = ""

    def to_dict(self) -> dict:
        return asdict(self)

    @classmethod
    def from_dict(cls, d: dict) -> "AnalysisResult":
        return cls(**{k: v for k, v in d.items() if k in cls.__dataclass_fields__})

    @classmethod
    def failed(
        cls,
        entry_index: int,
        timestamp: str,
        input_type: str,
        error: str,
    ) -> "AnalysisResult":
        """Create a failed result placeholder."""
        return cls(
            entry_index=entry_index,
            timestamp=timestamp,
            input_type=input_type,
            description="",
            category="",
            key_elements=[],
            analyzed_at=now_iso(),
            status="failed",
            error=error,
        )


# ---------------------------------------------------------------------------
# AnalysisStore — manages per-pipeline ai_analyses.json
# ---------------------------------------------------------------------------


class AnalysisStore:
    """Read / write the ``ai_analyses.json`` file for one pipeline.

    File schema::

        {
          "pipeline_name": "...",
          "model": "...",
          "analyses": [ {AnalysisResult dict}, ... ],
          "daily_summary": null | "..."
        }
    """

    def __init__(self, analyses_path: Path, pipeline_name: str = "") -> None:
        self._path = analyses_path
        self._pipeline_name = pipeline_name

    # -- core I/O ---------------------------------------------------------

    def load(self) -> dict:
        """Load the analyses file (or return a fresh skeleton)."""
        if self._path.exists():
            return read_json(self._path)
        return {
            "pipeline_name": self._pipeline_name,
            "model": "",
            "analyses": [],
            "daily_summary": None,
        }

    def _save(self, data: dict) -> None:
        write_json(self._path, data)

    # -- mutations --------------------------------------------------------

    def append(self, result: AnalysisResult) -> None:
        """Append an analysis result (thread-safe when used via queue)."""
        data = self.load()
        data["analyses"].append(result.to_dict())
        self._save(data)

    def set_model(self, model: str) -> None:
        data = self.load()
        data["model"] = model
        self._save(data)

    def set_daily_summary(self, summary: str) -> None:
        data = self.load()
        data["daily_summary"] = summary
        self._save(data)

    # -- queries ----------------------------------------------------------

    def get_by_entry_index(self, index: int) -> Optional[dict]:
        data = self.load()
        for a in data.get("analyses", []):
            if a.get("entry_index") == index:
                return a
        return None

    def has_analysis(self, entry_index: int, timestamp: str) -> bool:
        """Check if an entry already has an analysis (by index + timestamp)."""
        a = self.get_by_entry_index(entry_index)
        if a is None:
            return False
        return a.get("timestamp") == timestamp

    def get_all_completed(self) -> list[dict]:
        data = self.load()
        return [
            a for a in data.get("analyses", []) if a.get("status") == "completed"
        ]

    def get_all(self) -> list[dict]:
        data = self.load()
        return data.get("analyses", [])


