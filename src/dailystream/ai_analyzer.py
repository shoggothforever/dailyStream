# ---------------------------------------------------------------------------
# Image compression helper
# ---------------------------------------------------------------------------


def _compress_image(image_path: Path, max_size_kb: int = 150) -> bytes:
    """Compress an image to JPEG bytes, capped at *max_size_kb*.

    Falls back to reading the raw file when Pillow is unavailable.
    """
    if _PIL_AVAILABLE:
        from PIL import Image as PILImage

        img = PILImage.open(image_path)
        # Downscale to max 1280px width (preserves aspect ratio)
        max_width = 1280
        if img.width > max_width:
            ratio = max_width / img.width
            img = img.resize(
                (max_width, int(img.height * ratio)),
                PILImage.Resampling.LANCZOS,
            )
        # Convert RGBA → RGB for JPEG
        if img.mode in ("RGBA", "P"):
            img = img.convert("RGB")

        import io

        quality = 85
        while quality >= 20:
            buf = io.BytesIO()
            img.save(buf, format="JPEG", quality=quality, optimize=True)
            if buf.tell() / 1024 <= max_size_kb:
                return buf.getvalue()
            quality -= 10

        # Worst case
        buf = io.BytesIO()
        img.save(buf, format="JPEG", quality=20, optimize=True)
        return buf.getvalue()

    # No Pillow — read raw bytes
    return image_path.read_bytes()


def _encode_image_base64(image_path: Path, max_size_kb: int = 150) -> str:
    """Return a base64-encoded string of the (optionally compressed) image."""
    raw = _compress_image(image_path, max_size_kb)
    return base64.b64encode(raw).decode("ascii")


def _detect_media_type(image_path: Path) -> str:
    suffix = image_path.suffix.lower()
    mapping = {
        ".png": "image/png",
        ".jpg": "image/jpeg",
        ".jpeg": "image/jpeg",
        ".gif": "image/gif",
        ".webp": "image/webp",
    }
    return mapping.get(suffix, "image/png")


