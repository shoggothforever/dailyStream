"""AI image and URL analysis engine for DailyStream.

This module provides AI-powered analysis of screenshots and URLs using the
Anthropic Claude API.  It is designed with a **data-separation architecture**:
AI analysis results are stored in a separate ``ai_analyses.json`` file per
pipeline, linked to the original ``context.json`` entries via
``entry_index + timestamp`` indexing.

Two analysis modes are supported:

- **realtime**: each entry is analysed independently right after it is
  captured.  A background serial queue (``AnalysisQueue``) ensures
  ``ai_analyses.json`` writes are never concurrent.
- **daily_report**: analysis happens in batch at workspace-end time.
  An additional cross-pipeline summary is generated by AI.

The ``anthropic`` SDK is an *optional* dependency.  When it is not
installed every public function degrades gracefully (returns ``None`` /
does nothing) and emits a single warning.
"""

