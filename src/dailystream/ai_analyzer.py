def batch_analyze_workspace(
    config: "Config",
    workspace_dir: Path,
    workspace_meta,
) -> bool:
    """Daily-report mode: batch-analyse all un-analysed image/url entries.

    Iterates each pipeline, reads ``context.json`` + ``ai_analyses.json``,
    finds entries without analysis, and calls the AI for each batch.

    Returns ``True`` if any new analyses were produced.
    """
    from .pipeline import PipelineManager

    if not _ANTHROPIC_AVAILABLE:
        _warn_no_anthropic()
        return False

    analyzer = ImageAnalyzer(config)
    if not analyzer.available:
        return False

    pm = PipelineManager(workspace_dir)
    any_new = False

    for pipeline_name in pm.list_pipelines():
        entries = pm.get_entries(pipeline_name)
        store_path = (
            workspace_dir / "pipelines" / pipeline_name / "ai_analyses.json"
        )
        store = AnalysisStore(store_path, pipeline_name=pipeline_name)

        # Build pipeline-level context for daily_report
        meta = pm.get_pipeline_meta(pipeline_name)
        ctx_parts = [f"Pipeline: {pipeline_name}"]
        if meta.get("description"):
            ctx_parts.append(f"Description: {meta['description']}")
        if meta.get("goal"):
            ctx_parts.append(f"Goal: {meta['goal']}")
        pipeline_context = "\n".join(ctx_parts)

        # Collect un-analysed image/url entries
        to_analyze: list[tuple[int, dict]] = []
        for idx, entry in enumerate(entries):
            itype = entry.get("input_type", "")
            ts = entry.get("timestamp", "")
            if itype in ("image", "url") and not store.has_analysis(idx, ts):
                to_analyze.append((idx, entry))

        if not to_analyze:
            continue

        # Batch in groups of ai_batch_size
        batch_size = getattr(config, "ai_batch_size", 10)
        for batch_start in range(0, len(to_analyze), batch_size):
            batch = to_analyze[batch_start : batch_start + batch_size]
            batch_entries = [e for _, e in batch]

            results = analyzer.batch_analyze_entries(
                batch_entries,
                pipeline_name=pipeline_name,
                pipeline_context=pipeline_context,
            )

            for (idx, entry), result in zip(batch, results):
                ts = entry.get("timestamp", "")
                itype = entry.get("input_type", "")
                if result is None:
                    result = AnalysisResult.failed(
                        entry_index=idx,
                        timestamp=ts,
                        input_type=itype,
                        error="AI analysis returned None",
                    )
                else:
                    result.entry_index = idx
                    result.timestamp = ts
                store.append(result)
                any_new = True

        store.set_model(analyzer._model)

    return any_new


def generate_daily_summary(
    config: "Config",
    workspace_dir: Path,
    workspace_meta,
) -> Optional[str]:
    """Daily-report mode: generate a cross-pipeline work summary.

    Reads each pipeline's ``ai_analyses.json``, builds a text-only
    prompt with all descriptions, and asks the AI for a daily summary.
    Saves the result to ``<workspace>/ai_daily_summary.json``.

    Returns the overall summary text, or ``None`` on failure.
    """
    from .pipeline import PipelineManager

    if not _ANTHROPIC_AVAILABLE:
        _warn_no_anthropic()
        return None

    analyzer = ImageAnalyzer(config)
    if not analyzer.available:
        return None

    pm = PipelineManager(workspace_dir)
    pipeline_texts: list[str] = []

    for pipeline_name in pm.list_pipelines():
        store_path = (
            workspace_dir / "pipelines" / pipeline_name / "ai_analyses.json"
        )
        store = AnalysisStore(store_path, pipeline_name=pipeline_name)
        completed = store.get_all_completed()

        meta = pm.get_pipeline_meta(pipeline_name)

        lines = [f"### Pipeline: {pipeline_name}"]
        if meta.get("description"):
            lines.append(f"Description: {meta['description']}")
        if meta.get("goal"):
            lines.append(f"Goal: {meta['goal']}")
        lines.append(f"Entries analysed: {len(completed)}")
        for a in completed:
            lines.append(f"- [{a.get('category', '?')}] {a.get('description', '?')}")
        pipeline_texts.append("\n".join(lines))

    if not pipeline_texts:
        return None

    pipeline_data_text = "\n\n".join(pipeline_texts)
    summary_dict = analyzer.generate_daily_summary_text(pipeline_data_text)

    if summary_dict is None:
        return None

    # Save to workspace-level file
    summary_data = {
        "generated_at": now_iso(),
        "model": analyzer._model,
        "pipeline_summaries": summary_dict.get("pipeline_summaries", {}),
        "overall_summary": summary_dict.get("overall_summary", ""),
    }
    write_json(workspace_dir / "ai_daily_summary.json", summary_data)

    # Also write per-pipeline summaries back into their ai_analyses.json
    for pipeline_name, ps in summary_dict.get("pipeline_summaries", {}).items():
        store_path = (
            workspace_dir / "pipelines" / pipeline_name / "ai_analyses.json"
        )
        if store_path.exists():
            store = AnalysisStore(store_path, pipeline_name=pipeline_name)
            store.set_daily_summary(ps)

